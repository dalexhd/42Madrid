/*
** @author	aborboll
** @param	{t_llong nb}	The number
** @desc	Get number length
** @returns	int
*/

#include "../includes/libft.h"

int		ft_numlen(t_llong nb)
{
	int		i;

	i = 0;
	if (nb < 0)
	{
		nb *= -1;
		i++;
	}
	while (nb > 0)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
